package cn.edu.guet.model;

public class Vip {
    private String vipId;
    private String vipName;
    private String phone;
    private String cardType;
    private Float cardMoney;
    private Float consumeMoney;
    private Float comMoney;

    public String getVipId() {
        return vipId;
    }

    public void setVipId(String vipId) {
        this.vipId = vipId;
    }

    public String getVipName() {
        return vipName;
    }

    public void setVipName(String vipName) {
        this.vipName = vipName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCardType() {
        return cardType;
    }

    public void setCardType(String cardType) {
        this.cardType = cardType;
    }

    public Float getCardMoney() {
        return cardMoney;
    }

    public void setCardMoney(Float cardMoney) {
        this.cardMoney = cardMoney;
    }

    public Float getConsumeMoney() {
        return consumeMoney;
    }

    public void setConsumeMoney(Float consumeMoney) {
        this.consumeMoney = consumeMoney;
    }

    public Float getComMoney() {
        return comMoney;
    }

    public void setComMoney(Float comMoney) {
        this.comMoney = comMoney;
    }
}
